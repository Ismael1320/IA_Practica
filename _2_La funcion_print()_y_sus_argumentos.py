#Python ofrece otro mecanismo para el paso de argumentos, que puede ser útil cuando 
# deseas convencer a la función print() para que cambie un poco su comportamiento. Uno de 
# ellos 'end': cualquier argumento de palabra clave debe colocarse después del último 
# argumento posicional; y sep que funciona como separador


# Esta linea lo que hace es imprimer un los textos que aparecen con doble comilla, con una 
# separación indicadada por sep que sera *** y al final del argumento genera ...
print("Programming","Essentials","in",sep="***",end="...")
