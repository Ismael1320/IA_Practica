x =  0 #declaramos un valor para x
x = float(x) #declaramos que el valor de x va hacer flotante(decimal)
y = 3*(x**3) - 2*(x**2) + 3*(x) - 1 #declramos un valor para "y" con la siguiente opreción la cual necesita el valor de la variable x  
print("y =", y) #Imprime el valor de "y" en decimales ya que x se habia declarado como flotante

x = 1 #declaramos una nueva variable x con otro valor diferente
